            <h4>Overview</h4>

<p>The RCS Community Tournament program is a system designed to provide community content creators the ability to host esports events under their brand name for their communities. The RCS partners with content creators in order to host mutally benifical community events. The RCS will provide solutions to tournament logistics while the content creator advertises the events. The RCS reserves the right to accept or deny any license for any reason. If an application is accepted the RCS will run the event free of charge.</p><br />
<p>Content Creators refers to individuals who manage a community, guidelines for communities are as follows:</p>
         <ul>
					<li>Communities should have over 5,000 followers or equivalent</li> 
					<li>Twitch streamers should have at least 100 viewers</li>					 
					</ul>  
            <h4>Eligibility</h4>

<p>Community content creators such as twitch streamers and youtubers are eligible for the RCS licensing program with the exception of: </p>
<ul>
					<li>Individuals specifically prohibited from participation by Riot Games or Blizzard policy </li> 
					<li>Anyone prohibited by local laws, the Tournament Organizer, rules, or venue management </li>					 
					</ul>
<br />

 <h4>Prizes</h4>

<p>The RCS may also offer prizes in addition to the content creator. </p>
<p>League of Legends prizes</p>
<ul>
					
                    <li>Riot points</li> 
					<li>Influence Points Booster</li> 
					<li>Triumphant Ryze</li> 
					 
					</ul></p><br>
<h4>Marketing and Branding</h4>

<p>Events the RCS hosts for content creators should be advertised by the content creator to their community. The RCS reserves the right to fulfill sponsor advertising obligations and may develop advertisements to be distributed to the community by the content creator.  </p>
<h3 class="headline">Content Creator Options</h3>
             <h4>Frequency</h4>

<p>Content Creators have the option to host an event anywhere from once a month to four times a month. The current limitations on frequency are temporary and designed to maintain quality control.</p>

<h4>Broadcasting</h4>
<p>The RCS maintains a full broadcasting crew and is able to broadcast the event to either the RCS channel or the Content Creators channel. This option should only be utalized if a content creator decides to either not personally boradcast the event or wishes to join the RCS casters in an RCS production. </p>
<br />

<h3 class="headline">RCS Twitch Wars</h3>
            
<h4>Invites</h4>

<p>Each content creator enrolled in the Community Tournament program will recieve <em>one</em> invite to a special event. The invite allows the content creator to send representatives of their community to compete in a special tournament. The content creator may not compete in this event.  </p>
            
<h4>Prizing</h4>

<p>Prizing for events will vary however content creators will recive 20 percent of their teams prize package.</p>

