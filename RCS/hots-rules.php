<h2><em>Tournament Administrative Rules</em></h2>
<h3>Competitor Code of Conduct</h3>
<p>Players are expected to maintain a level of civility toward other competitors and the RCS Heroes of the Storm administration. Violations of this rule will be dealt with on a case by case basis, and repeated failures to abide may result in penalties toward the associated team.</p>
<p>Players are expected to act in accordance with general good will. We will not tolerate any toxic behavior. Those found disrupting the order of the tournament or harassing a player in any way will be removed from the tournament while more egregious conduct may result in additional actions taken against both the player and their team if applicable.</p>
<p>Any attempt to deceive other players or the administration with false information, fake accounts, teams or matches will result in a lifetime ban from all RandomCS activities. As a community, we do not want such people playing.</p>
<p>Cheating or using any known exploits or bugs to your advantage in an RCS Heroes of the Storm match will result in forfeit from the tournament, a season ban and point forfeiture. Any further infractions after the ban period will result in a lifetime ban.</p>
<p><em>Ignorance of the rules is not an excuse.</em></p>
<h3>Roster</h3>
<p>All players must be added to the team on battlefy.com prior to the start of a season, this includes up to two stand-in substitutes players.  Points are tied to the Team Captain and the Team Name for the season.  A Tournament Admin will need to be notified of any Team Name changes for points to transfer.</p>
<h3>Team Drafting and Map Selection</h3>
<p>The Team assigned to the top of the bracket will handle the hero draft or hero drafts of all the rounds.  The Team assigned to the bottom of the bracket will host the game or games of all the rounds.  The drafting and map selection is handled by <a href="www.heroesdraft.com">HeroesDraft.com</a>. The Battleground is determined by map veto through the map drafting process for Bo1 or the first round of Bo3.  The losing team in a Bo3 will choose between selecting the map or having first pick.  The draft / ban phase should be resolved within a ten minute time frame.</p>  
<p>Matches are expected to begin shortly after the draft finishes. </p>

<h3>Drafting Proceedings</h3><br>
<ol>
<li>Go to <a href="www.heroesdraft.com">HeroesDraft.com</a>.</li>
<li>Start by clicking the Create Draft Session button at the top of the page.</li>
<li>Type in the teams names and tournament name NexVerse</li>
<li>If the round is Bo1 or the first round of a Bo3, choose Map Draft (VETO), starting team preference of Random and Number of bans at 2. If the round is not the first round of a Bo3, then select the map based on the selection by the appropriate team.</li>
<li>Share the corresponding links to the drafting session with opposing team captain, observers and/or casters.</li>
</ol>

<h3>Server</h3>
<p>All RCS matches unless otherwise noted have to be played on the NA servers.</p>
<h3>Failure to appear / No Show</h3>
<p>Each team has 15 minutes to field a full team.  Please report no-show teams to Tournament Admins in Discord. Team Captains should contact the Tournament Admins if conflicts arise due to prior engagements.</p>
<h3>Penalty for dropping out from a tournament</h3>
<p>If a team drops from a weekly event or violates the 15 minute match start rule, they will not be permitted to re-enter that week's tournament.</p>
<h3>Protest</h3>
<p>A protest should be made through the Discord server, a private message may be sent to an admin if public chat is not desired.</p>
 
<p>If a player wants to submit a protest concerning a specific match, the protest needs to be filed before the start of the match winners next game.</p> 
<h3>Pause Rules</h3>
<p>Game pauses should only occur if a player is having connection issues. If more than ten minutes are needed, petition an admin in IRC and include what the circumstances are and what is being done to try and fix them. Teams will announce unpausing prior to actually doing so.</p>

<h3>Spirit of the Game / Rules</h3>
<p>The Tournament Administration reserves the rights to make judgment on extenuating circumstances that come up as they deem fit. Any major changes to rules will be announced, and the rules will be amended accordingly.  Any ineligible heroes or maps (normally due to known bugs) will be announced in Discord during the check in phase.</p>
<h3>Score Reporting</h3>
<p>After a match is concluded the game replay must be uploaded to the Battlefy match reporting screen provided on the tournament website. The winning team will submit the score.  Teams are to keep a copy of a replay for at least two weeks, in the event of a formal protest.</p>
To report a score:  
<ol><br>
<li>Find the match on the tournament bracket and left click to select that match.</li>
<li>Click the <em>Choose File</em> and navigate to where the replay files are saved.</li>
<li>Click <em>OK</em> and upload the file.</li>
</ol><br>
<p>Any problems or trouble uploading, please contact a Tournament Admin.</p>

<h3>Rematch</h3>
<p>In the event of a tournament running exceptionally late, players can petition Tournament Admins to finish earlier the next evening. This is at the admins discretion to allow.</p>

<h3>Spectators</h3>
<p>General spectators will not be allowed in a game. The only authorized spectators are Tournament Administration or official broadcasters.</p>

<h3>Prize Distribution</h3>
The Team Captain will be responsible for the distribution of the prize and will be paid out through Paypal.
<h3>Point System</h3>
<p>Teams will be awarded points based on what round of each weekly tournament they make it to, with bonuses awarded for every week of a season they compete in as well as a stacking bounty for beating any team that wins at least two tournaments in a row. At the end of the six weeks, the top 8 teams by total points will be invited to compete in the prize tournament, and have 95% of their points deducted from their totals.</p> 

<p>When the next season begins, none of the other teams that missed the top 8 will have their point totals reset and weekly play begins once more. Teams are eligible to carry over up to two seasonâ€™s worth of points forward, which should allow more teams access to the prize tournament while the more dominant teams need to stay that way in order to get back to the top.
The weekly, regular season matches will follow the format of single game matches until the top four when it moves to best of 3, while the playoff tournament will be best of three from start to finish and will not award any points.</p>

<h3 class="headline">General Rules</h3>
<h3>Tournament Direction</h3>
<p>The RCS Heroes of the Storm is organized by the <a href="www.randomcs.com">RandomCS.com</a> Heroes of the Storm division (RCS).</p>
<h3>Rule modifications</h3>

<p>The RCS Heroes of the Storm administration reserves the right to change or modify these rules, and may at their discretion choose to make decisions not explicitly outlined by these rules in order to keep things fair and with good sportsmanship.</p>
<h3>E-Mail</h3>
