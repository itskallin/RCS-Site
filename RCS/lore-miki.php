<p>Not your typical California pink, Miki often complains about living in a giant tourist trap. Hailing from the sunny shores of San Francisco, Miki has always appreciated the eclectic mix of cultures found within her hometown. Although she will never openly admit her endearment for San Fran, Miki can on rare occasions be found wandering the different piers, taking in the sights of the busy, California day. Moreover, Miki has also contributed greatly to San Francisco's street art scene. Creating graffiti masterpieces from spray paint is one of her all-time favorite activities. </p>

<p>Miki also enjoys spending long hours chatting with her gregarious best friend, Lauren Spark . . . over the internet. According to Miki, hanging out with Lauren in person can be a "trying ordeal," "so much talking," and "where is the escape button?" She has no further comment on the matter. </p>

<p>When she is not chatting with Lauren or reading <em>manga</em>, Miki can be found eagerly battling opponents in the esports' universe. She describes spending most of her time "staring at a giant computer screen and clicking buttons with unprecedented speed." Miki also admits that her love of online gaming has deterred her from going out to the pier and feeling the warmth of the sun and a fresh ocean breeze in "a month or two. Okay, maybe four months." </p>

<p>Miki considers her best gaming qualities to be her competitive nature, fast clicking, and biting sarcasm. Although she prefers questing alone, Miki is also a great team member. During a tournament match, Miki consistently ensures that all of her teammates are safe and accounted at any given moment. If a team member is lagging behind or surrounded by the enemy, Miki does not hesitate to aid her distressed comrade, even at great personal risk. Above all, Miki is a warrior with a noble heart. However, her biting sarcasm often erases any thankfulness her teammates may be feeling towards her.</p>

