            <h2>Eligibility</h2>
<p>Anyone is eligible to play in a sanctioned tournament, with the exception of:
<ul>
<li>Individuals specifically prohibited from participation by Blizzard Entertainment policy</li>
<li>Anyone prohibited by local laws, the Tournament Organizer, rules, or venue management</li>
<li>Tournament officials can not participate in events they are actively officiating</li>
<li>Individuals specifically prohibited from participation by Random Championship Series</li>
</ul></p>
<p>
Anyone is eligible to participate as a tournament official for a tournament with the exception of:
<ul>
<li>Individuals specifically prohibited from participation by Blizzard Entertainment</li>
<li>Individuals specifically prohibited from participation by the tournament’s attendance policy</li>
<li>Anyone planning to play in the tournament</li>
</ul>
</p>
<p>Tournament officials and support staff can not arbitrarily prohibit anyone who is not in violation of the above from participating in a tournament.</p>

<h2>Procedures</h2>
<h3>General Rules</h3>
<p>All players  are required to be in the RCS Hearthstone Discord Channel. Players who are unreachable by the admins could be disqualified during a dispute.</p>
<p><strong>Players must take screenshots of their victories as a proof. An admin requires a screenshot proof to settle a win dispute</strong></p>
<p>BattleTags must be correct. In the event a player registers with the incorrect BattleTag, RCS staff will correct the BattleTag if and only if no matches have been completed.</p>
<p>Players must declare three deck classes at registration, failure to do so will result in disqualification. Players must use the same deck lists/types for the whole night (ex. you may not use face hunter one round and midrange hunter the next round).</p>
<p>Winning players within a Best of Three must switch decks; losing players may remain on the same deck, although they are not obligated to.</p>
<p>This means that you must win with two different decks in a Best of Three. All matches except for semi finals and grand finals are Best of Three; semi and grand finals are Best of Five, which means that you must win with three different decks.</p>
<p>There are no deck bans.</p>
<p>No tournament tampering. Players using exploits will be DISQUALIFIED!</p>
<p><strong>Player must provide a screenshot of their challenge screen with their opponent if it is requested. The screenshot must show only one deck for each of the classes you registered with. If the player doesn’t want to delete their other decks, they can take out a single card from them making it invalid.</strong></p>
<p>If any player has an illegal challenge screen, the first offense will be forgiven, but the second offense will result in a disqualification. </p>
<p>Players have 10 minutes to check into a match or they will automatically forfeit the match. After checking into a match players have 5 minutes to communicate with their opponent and begin the match. Players who are unreachable for 5 minutes after checking into a match will incur a game loss for each 5 minutes over the specified time. Players are allowed to request breaks but breaks must be approved by an admin. Upon requesting a break the admin will notify players on the length of the break.</p>


<h3>Dropping from a Tournament</h3>
<p>Dropping from the tournament indicates to the tournament officials that your team no longer wants to continue participating.</p>

<p>Teams can drop from the tournament at any time by notifying a Tournament Officials.</p>

<p>Dropping from a tournament does not prevent tournament officials from assigning any penalties to a player or remove any penalties a player may have already incurred.</p>

<p>If a team does not show up or is not logged in and ready at the designated set up time, the RCS tournament officials may drop players in order to keep things on schedule for the rest of the competitors. If both players are no-shows, they might both get dropped from the bracket.</p>


<h3>Spectating and Broadcasting</h3>
<p>In-Game Spectating: Will be prohibited unless both player of the match has given the permission to spectate. Matches can be viewed via the RCS Website or twitch channel.</p>
<p>Is allowed as long as spectators do not interfere with the tournament proceedings. Spectators must obey the following rules:<ul>
<li>Anyone not currently participating in a match can watch other players in a game</li>
<li>Spectators can not communicate with players currently in a match by any means</li>
<li>If a spectator sees someone breaking the rules, they should report it to the tournament organizer or an admin.</li>
</ul></p>

<h2>Behavior</h2>
<h3>Player Ethical Obligation</h3>
<p>Sportsmanlike behavior is the responsibility of all tournament participants and promotes healthy competition among players. Failure to comply could result in RCS officials asking/telling a player to leave the tournament / all RCS Events, and being barred from participation in other RCS or HCT events.</p>
<p>All tournament participants, including players, admins and the tournament organizer must read and adhere to the RCS Ethical Obligation, inside and outside the game.</p>

<h3>Tournament Officials</h3>
<p>Tournament organizers & admins need to lead by example and adhere to the behavior guidelines outlined in this document. This ensures a fair and consistent experience for all players during the tournament.</p>
<p>Tournament organizers and admins must be impartial and transparent when making official decisions. They must communicate clearly with the players, especially when issuing specific instructions or penalties.</p>

<p>Tournament organizers and admins must communicate in good faith any tournament decisions they make. All tournament officials have the same goal: to provide a fair and consistent experience for all players participating in their tournament.</p>

<p>Admins are the final authority for determining rules infractions and administering penalties. In addition, admins are the final authority for enforcing tournament procedures. These include, but aren’t limited to: administering penalties rules infractions, tracking player’s hero rosters, enforcing format deck selection rules, correctly guiding end of game procedures, and ensuring match outcomes are correctly recorded.</p>
<p>Tournament organizers are the final authority for decisions involving tournament logistics. Logistics include, but are not limited to: venue selection, managing shoutcasters, ensuring provided tournament equipment is in working order, tournament registration, matchmaking, tournament length and timing, and prize structures.</p>

<h3>Player Communication</h3>
<p>Players have a responsibility to follow the communication guidelines outlined below. </p>

<p>Appropriate communication with the tournament officials, team captains and one another leads to a more transparent tournament experience for everyone involved.</p>

<p>Players can communicate with other players on the as long as they don’t violate any of the rules outlined in this document.</p>

<h2>Tournament Feedback and Complaints</h2>
<p>Players can appeal a decision by any referee to the Head Referee. Pertaining to tournament rules the Head Referee's decision over rules any decision made by a referee during the course of the tournament. In the event the Head Referee cannot be reached to make a decision, the League Commissioner or Vice League Commissioner may make a proxy ruling.</p>
<p> Event feedback can be submitted on the <a href="info.php">info portal</a> via the help button.</p>

<h2> Admins </h2>

<h3>Penalties</h3>
<p>Refs who observe infractions during the course of the tournament should issue penalties based on the guidelines in this document. Venue owners, tournament organizers or any other tournament officials should inform a referee instead of issuing penalties themselves.</p>
<p>Referees should communicate the infraction, penalty and any other pertinent info to the offending player, their team and the team’s opponent in a clear, concise manner. </p>

<p>Refs should always determine an infraction first and then apply the prescribed penalty.</p>

<p>Refs need to make unbiased decisions, so the player or team’s skill level shouldn’t hold any bearing on infractions and penalties.</p>

<p>Penalties can apply to either a single match or persist for the duration of the tournament.</p>

<p>Teams cannot waive penalties assigned to their opponents.</p>

<p>Penalties include the following:</p>

<ul>

<li>Warning: an official, recorded notice to the player for a minor tournament infraction. It is important to record warnings to ensure further infractions get assigned appropriately escalated penalties.</li>

<li>Game loss: severe infractions may force a player to take an automatic loss in a current or upcoming game.</li>

<li>Disqualification: the most egregious infractions might warrant disqualification from the tournament. A player disqualified from a tournament forfeits any prizes they haven’t already received, unless the disqualification was the result of a series of escalating penalties throughout the tournament. A player disqualified due to escalating penalties will still receive prizes based on their final standing.</li>
</ul>

<p>Unless noted otherwise, successive penalties for the same infraction should follow the following escalation path: warning—warning—ban loss—game loss—match loss— disqualification.</p>


<h2>Tournament Violations</h2>
<h3>Tardiness</h3>
<p>This infraction occurs when the player is not ready to play at the beginning of a game.</p>

<p>Players need to be on time and ready to play when each game starts. When players are late, they delay the whole tournament.</p>

<p>If a player is more than 15 minutes late to their match, they will be disqualified.</p>

<h3>Outside Assistance</h3>
<p>Outside assistance occurs when a player communicates with spectators, or anyone else during a game and, at a referee’s determination, inadvertently gains an advantage over their current opponent.</p>
<p>This infraction assumes the offending player wasn’t deliberately trying to cheat.</p>
<p>Intentionally looking for any unfair advantage during the match is covered under Unsporting Conduct – Cheating.</p>
<p>The penalty for outside assistance is a warning.</p>

<h3>Failure to follow Announcements</h3>
<p>Every player has the responsibility to follow official tournament instructions and announcements. If officials have to repeat themselves over the course of running a tournament, it can cause delays and unwarranted disputes over the rules. The penalty for failing to follow official tournament announcements is a first game loss.</p>

