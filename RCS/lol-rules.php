            <h4>Eligibility</h4>

<p>Anyone is eligible to play in a sanctioned tournament, with the exception of: </p>
<ul>
					<li>Individuals specifically prohibited from participation by Riot Games policy </li> 
					<li>Anyone prohibited by local laws, the Tournament Organizer, rules, or venue management </li> 
					<li>Anyone (including temporary and contract workers) employed by Riot Games. Tournaments 
specifically identified ahead of time as “play with or against Riot Employees” are an exception to 
this rule </li> 
					 <li>Tournament officials can not participate in events they are actively officiating </li> 
					</ul>
<p>Anyone is eligible to participate as a tournament official for a tournament with the exception of:</p>
					<ul> 
					<li>Individuals specifically prohibited from participation by Riot Games </li> 
					<li>Individuals specifically prohibited from participation by the tournament’s attendance policy</li>  
					<li>Anyone planning to play in the tournament</li> 
					</ul>
		<p>Tournament officials and support staff can not arbitrarily prohibit anyone who is not in violation of the 
above from participating in a tournament. 
</p><br />
<h3 class="headline">Procedures</h3>
             

<p><strong>Play Stoppage:</strong> Players are allowed to pause if directed by a tournament official or as part of their allotted pause time 
per game. During any pause or stoppage, players can not leave the match area unless they are officially 
authorized. </p>
<p><strong>Side selection:</strong> Teams with the higher seed in a match are Team 1 in lobby or Blue Side.</p>


<ul>
 <li><strong>Directed pause:</strong> Officials may pause a game at their discretion.</li><br>

<li><strong>Team pause:</strong> Each team has a total of five (5) minutes’ pause time during a match for the 
following reasons: 
  <ul>
<li>An unintentional disconnection</li>
<li>A hardware or software malfunction (e.g. monitor power or peripheral disability, game 
glitch)</li>
</ul>
</li>  
<p>In an extraordinary circumstance, players may ask tournament referees for additional pause time after 
their five minutes have expired. In this case, the team must alert a referee and continue playing until the 
referee decides whether or not to grant the pause. At that point, granting the pause is at the sole 
discretion of the referee. If the official determines that a pause can not resolve the issue in a reasonable 
period of time, the player’s team will have to continue playing. </p>

<li><strong>Resuming the game:</strong> The pausing team can not resume play until they have clearance from an 
official and all players are ready at their stations.</li> 
<li><strong>Unauthorized pause:</strong> If a player pauses or unpauses a game without permission outside of their 
allotted pause time, or does not resume the game within their allotted pause time, they will be 
subject to penalties according to the tournament rules. 
</li>
<li><strong>Player communication during stoppage:</strong> Unlike the LCS, players are allowed to discuss 
gameplay or strategy with their teammates during a pause. 

</li></ul><br />

            <h4>Forced Ending</h4>

<p>In certain cases RCS, officials may need to end a game or a match before its normal conclusion. Typical 
causes for this include logistical difficulties (such as venue closure). RCS staff will use the 
following method to determine the match winners where a natural conclusion of the game is not 
possible. </p>

<p>Referees must give a 10-minute notice to players if they intend to use a forced ending to the game. 
Combined with the 10-minute notice to players, the game must last at least 20 minutes. This means that 
the earliest a referee may give the 10-minute notice to players in a game is when the game clock 
reaches 10 minutes. </p>
<p>After 10 minutes expire, the team with more gold, using in game rounding methods (typically via 
spectator mode) is the winner of the game.</p> 
 
<p>If the gold differential is tied, the referees must use tiebreakers to determine the winner of the game in 
the following order: </p>
<ul>
<li>The team with the most towers killed 
</li>
<li>The team with the most kills</li>
<li>The team with the most gold earned from the topmost gold earner. If that is tied, compare the 
two next highest gold earners from each team. If that is tied, continue comparing the third, 
fourth, and fifth gold earners from each team, in that order, until one of them is not tied. 
</li>
<li>In the event that all of the above metrics are tied, the tournament organizer must randomly 
determine a winner of the game via a random number generator on stream. Blue team will be even numbers and Purple team will be odd numbers.</li>
</ul><br />

            <h4>Match Reporting</h4>

<p>Once a match is completed, RCS staff will record the match via screenshots from spectating officials. The match results will then be published on the RCS website.  </p>
<br>

<h4>Dropping from a Tournament</h4>

<p>Dropping from the tournament indicates to the tournament officials that your team no longer wants to 
continue participating. Teams can drop from the tournament at any time by notifying a Tournament Officials. 
 </p>
 <p>Dropping from a tournament does not prevent tournament officials from assigning any penalties to a 
player or remove any penalties a player may have already incurred. </p>
<p>If a team does not show up or is not logged in and ready at the designated set up time, the RCS tournament 
officials may drop the team in order to keep things on schedule for the rest of the competitors. If both 
teams are no-shows, they might both get dropped from the bracket, so be punctual! </p>
<p>If an individual player leaves the tournament, he/she can not be removed from the team’s roster until after the 
tournament is over and the prizes have been awarded. If the number of players on the team drops below 
the minimum required for a specific game mode, the RCS tournament officials will drop the team from the 
tournament.</p> 
<p>If a team wants to drop or if tournament officials need to drop a team during a match, the team has to 
forfeit the current match before they can officially drop out of the tournament. All drops have to be 
publicly announced to other competitors. </p><br>
<h4>Spectating and Broadcasting</h4>

<p><strong>In-Person Spectating:</strong> Is allowed as long as spectators do not interfere with the tournament proceedings. 
Spectators must obey the following rules:  </p>
<ul>
<li>Anyone not currently participating in a match can watch other players in a game</li>
<li>Spectators can not communicate with players currently in a match by any means</li>
<li>If a spectator sees someone breaking the rules, they should report it to the tournament 
organizer or referee</li>
</ul>
<p>Anyone who is not actively participating in the match can spectate in-game, provided he does not interfere 
with the tournament.</p>

<p><strong>In-Game Spectating:</strong> Will be disabled/prohibited. Matches can be viewed
via the RCS Website or twitch channel. </p><br>
<h3 class="headline">Behavior</h3>
<h4>Player Ethical Obligation</h4>
<p>Sportsmanlike behavior is the responsibility of all tournament participants and promotes healthy competition among players. Failure to comply could result in RCS officials asking/telling a team captain to bench a toxic player.</p>
<p>All tournament participants, including players, refs and the tournament organizer must read and adhere to the Summoner&rsquo;s Code, inside and outside the game. For the most part it&rsquo;s basic stuff aimed at encouraging people not to act like jerks.</p>
<h4 id="behavior">Tournament Behavior</h4>
 <p>Sportsmanlike behavior is the responsibility of all tournament participants and promotes healthy 
competition among players. Failure to comply could result in RCS officials asking/telling a team captain to bench a toxic player. </p>
<p>Players/teams who impeded tournament flow are susceptible to disqualification.</p>

<p>All tournament participants, including players, refs and the tournament organizer must read and adhere 
to the Summoner’s Code, inside and outside the game. For the most part it’s basic stuff aimed at 
encouraging people not to act like jerks.</p>
<p>Players in clear violation of the Summoner’s Code during a tournament can incur penalties, in addition 
to the normal in-game sanctions that come from Riot Games or the Tribunal. </p>
<a href="http://gameinfo.na.leagueoflegends.com/en/game-info/get-started/summoners-code/" class="btn-u btn-brd btn-brd-hover btn-u-dark btn-u-xs pull-right" target="new">Summoner's Code</a>
 <br>            
<h4>Tournament Officials</h4>
 <p>Tournament organizers & referees need to lead by example and adhere to the behavior guidelines 
outlined in this document. This ensures a fair and consistent experience for all players during the 
tournament. 
</p>
<p>Tournament organizers and referees must be impartial and transparent when making official decisions. 
They must communicate clearly with the players, especially when issuing specific instructions or 
penalties. 
</p>
<p>Tournament organizers and refs must communicate in good faith any tournament decisions they make. 
All tournament officials have the same goal: to provide a fair and consistent experience for all players 
participating in their tournament. </p>
<p>Referees are the final authority for determining rules infractions and administering penalties. In 
addition, refs are the final authority for enforcing tournament procedures. These include, but aren’t 
limited to: administering penalties rules infractions, tracking teams’ active rosters, enforcing first pick 
and side selection during games, ensuring champion drafting is correct, correctly guiding end of game 
procedures, and ensuring match outcomes are correctly recorded. 
</p>
<p>Tournament organizers are the final authority for decisions involving tournament logistics. Logistics 
include, but are not limited to: venue selection, managing shoutcasters, ensuring provided tournament 
equipment is in working order, tournament registration, matchmaking, tournament length and timing, 
and prize structures. 
</p><br> 
<h4>Player Communication</h4>
 <p>Players have a responsibility to follow the communication guidelines outlined below. Appropriate 
communication with the tournament officials, team captains and one another leads to a more 
transparent tournament experience for everyone involved. 

</p>
<p>All communication between players and the tournament officials has to go through the team captain. 
This reduces confusion and allows officials to efficiently disseminate instructions. 

</p>
<p>Players can communicate with teammates and players on the opposing team as long as they don’t 
violate any of the rules outlined in this document. 
 </p>
<p>Players in a match aren’t allowed to communicate with other team’s competitors, their team’s 
alternates, or any spectators. If they need to, players can communicate with the TOs through their 
captain. Communicating with players or spectators outside the current match is a rules violation and 
may carry penalties. 

</p><br>
<h4>Prize Splitting</h4>
 <p>The RCS leaves the decision up to team captains on how to distribute their prize; however only the 5 players that played in the finals will be delivered the prizes.

</p><br>
<h4>Tournament Feedback and Complaints</h4>
 <p>Players can appeal a decision by any referee to the Head Referee. Pertaining to tournament rules the Head Referee's decision over rules any decision made by a referee durring the course of the tournament. In the event the Head Referee can not be reached to make a decision, the League Commisioner or Vice League Commisioner may make a proxy ruling. 
</p><br>
<h3 class="headline">Referees</h3>
<h4>Penalties</h4>
 <p>Refs who observe infractions during the course of the tournament should issue penalties based on the 
guidelines in this document. Venue owners, tournament organizers or any other tournament officials 
should inform a referee instead of issuing penalties themselves. 
</p>
<p>Referees should communicate the infraction, penalty and any other pertinent info to the offending 
player, their team and the team’s opponent in a clear, concise manner. Referees may add additional 
pause time to a match in progress if they took longer than three minutes to issue a penalty. 
</p>
<p>Refs should always determine an infraction first and then apply the prescribed penalty.</p>
<p>Refs need to make unbiased decisions, so the player or team’s skill level shouldn’t hold any bearing on 
infractions and penalties. 
</p>
<p>Penalties can apply to the team as a whole, or to an individual within a team. Consequences can also 
apply to either a single match or persist for the duration of the tournament. 
</p>
<p>Teams can not waive penalties assigned to their opponents.</p>
<p>Penalties include the following: 
</p>
<p><strong>Warning:</strong> an official, recorded notice to the player or the team for a minor tournament infraction. It is 
important to record warnings to ensure further infractions get assigned appropriately escalated 
penalties.</p>
<p><strong>Ban loss:</strong> this penalty removes the offending team’s ability to ban one champion during the draft phase 
of the next round. To enforce this penalty, the ref should instruct the team not select their first ban and let the timer run 
out.</p>
<p><strong>Side selection loss:</strong> this penalty automatically passes the decision for draft order and side of the map to 
the opposing team. 
</p>
<p><strong>Game loss:</strong> severe infractions may force a team to take an automatic loss in a current or upcoming 
game. 
</p>
<p><strong>Match loss:</strong> even more severe infractions might force a team to forfeit their current match 
</p>
<p><strong>Disqualification:</strong> the most egregious infractions might warrant disqualification from the tournament. In 
most cases, the disqualification applies to the entire team. A team disqualified from a tournament 
forfeits any prizes they haven’t already received, unless the disqualification was the result of a series of 
escalating penalties throughout the tournament. A team disqualified due to escalating penalties will still 
receive prizes based on their final standing. 
</p>

<p>In certain cases, the referee has the option to disqualify a single player on the team instead of the entire 
team. The referee has this option only when he can determine that a player’s infraction did not affect 
the opposing team and the player committed the infraction without the involvement of the rest of the 
team. This typically happens when the player commits the Unsporting Conduct – Severe infraction. In 
this situation, the team may continue in the tournament if they have an available substitute player. If 
the team does not have a substitute player, the team may not continue in the tournament and must 
drop. 
</p>
<p>Unless noted otherwise, successive penalties for the same infraction should follow the following 
escalation path: warning—warning—side selection loss—ban loss—game loss—match loss—
disqualification. 
</p><br>
<h3 class="headline">Tournament Violations</h3>
<h4>Tardiness</h4>
 <p>This infraction occurs when the team’s not ready to play at the beginning of a game. Teams need to be 
on time and ready to play when each game starts. When teams are late, they delay the whole 
tournament.</p>
<p>Teams get a three minute grace period before they accrue one of two penalties. If the team is less than 
10 minutes late to the round, they receive a side selection loss. If the team is more than 10 minutes late, 
they receive a game loss penalty.</p><br>

<h4>Outside Assistance</h4>
 <p>Outside assistance occurs when a team communicates with spectators, opposing teams or anyone else 
during a game and, at a referee’s determination, inadvertently gains an advantage over their current 
opponent. 
</p>
<p>This infraction assumes the offending player wasn’t deliberately trying to cheat. Intentionally looking for 
any unfair advantage during the match is covered under Unsporting Conduct – Cheating. 
</p>
<p>The penalty for outside assistance is a warning. 
</p><br>

<h4>Failure to follow Announcements</h4>
 <p>Every player has the responsibility to follow official tournament instructions and announcements. If 
officials have to repeat themselves over the course of running a tournament, it can cause delays and 
unwarranted disputes over the rules. 
The penalty for failing to follow official tournament announcements is a first pick loss. 
 </p><br>
