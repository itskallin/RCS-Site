            <h2>Elegibilidad:</h2>
<p>Cualquiera es elegible para jugar en un torneo sancionado, excepto:
<ul>
<li>Individuos que tengan específicamente prohibida la participación por políticas de Blizzard Entertainment.
</li>
<li>Cualquier persona prohibida por leyes locales, los organizadores del torneo o los administradores del sitio donde se realice el evento.
</li>
<li>Los jueces del torneo no pueden participar en los eventos si están arbitrando mientras estos se desarrollan.
</li>
<li>Individuos que tengan específicamente prohibida la participación por Random Championship Series.
</li>
</ul></p>
<p>
Cualquiera es elegible para ser juez de un torneo excepto:

<ul>
<li>Individuos que tengan específicamente prohibida la participación por las políticas de asistencia del torneo.
</li>
<li>Individuos que tengan específicamente prohibida la participación por las políticas de asistencia del torneo.
</li>
<li>Cualquiera planeando jugar en el torneo.
</li>
</ul>
</p>
<p>Los jueces y personal no pueden prohibir a nadie, que no incumpla lo anterior, participar en un torneo.
</p>

<h2>Procedimientos:
</h2>
<h3>Reglas Generales</h3>
<p>Todos los jugadores deben estar en el canal de discord de RCS Hearthstone. Si un jugador no puede ser encontrado allí por un administrador podría ser descalificado durante una disputa.
</p>
<p><strong>Los jugadores deben tomar capturas de pantalla de sus victorias como prueba. Los administradores necesitan estas capturas como prueba para resolver una disputa.</strong></p>
<p>Los jugadores deben elegir 3 mazos de distintas clases al registrarse, el no hacerlo provocará una descalificación.
</p>
<p>Los jugadores deben usar los mismos mazos (Con la misma lista de cartas) toda la noche (Ej: Un jugador no puede usar Hunter 0Face una ronda y cambiar a MidRande Hunter la ronda siguiente).
</p>
<p>Las rondas se jugarán al mejor en 2 de 3 partidas; el jugador que gane un duelo no podrá volver a utilizar el mazo en la misma ronda, el jugador que pierda el duelo podrá decidir si volver a utilizar ese mazo o cambiarlo por otro de los 3 que declaró durante el registro.
</p>
<p>Esto significa que un jugador deberá ganar con dos mazos distintos en un «Mejor de 3» para avanzar. Todos los encuentros excepto las semifinales y finales serán al «Mejor de tres»; las semifinales y finales serán al «Mejor de cinco», lo que significa que un jugador deberá ganar con 3 mazos distintos para avanzar.
</p>
<p>No hay eliminación de mazos.
</p>
<p>¡Quien sea encontrado haciendo trampa o sobornando será DESCALIFICADO!
</p>
<p><strong>Los jugadores deberán dar una captura de pantalla de la sala de espera si su oponente la pide. Esta captura de pantalla deberá mostrar solamente un mazo de cada clase registrada. Si el jugador no quiere borrar sus otros mazos, puede sacar una carta de ellos, haciéndolos injugables.
</strong></p>
<p>Si algún jugador rompe esta regla más de una vez, será descalificado.
 </p>

<h3>Abandonar un torneo</h3>
<p>Abandonar un torneo indica a los jueces que su equipo ya no desea seguir participando.</p>

<p>Un equipo puede dejar un torneo cuando desee, notificándolo a los jueces.</p>

<p>Abandonar un torneo no evita que los jueces puedan dar o quitar penalizaciones a un jugador.</p>

<p>Si un equipo no se encuentra en el lugar para jugar a la hora asignada, los jueces descalificarán a los jugadores con el fin de mantener el horario en orden para los demás competidores. Si ambos equipos no se encuentran, ambos serán descalificados.</p>


<h3>Para espectar y/o transmitir un juego</h3>
<p>Espectar una partida durante el torneo está prohibido, a menos que ambos jugadores hayan dado permiso para esto. Los encuentros podrán ser vistos por medio del sitio web o del canal de Twitch de RCS.</p>
<p>Está permitido mirar juegos siempre y cuando los espectadores no interfieran en el torneo. Los espectadores están sujetos a las siguientes reglas:<ul>
<li>Cualquiera que no esté jugando una ronda podrá mirar a otros jugadores en juego.</li>
<li>Los espectadores no pueden comunicarse con los jugadores que observan competir por ningún medio.</li>
<li>Si un espectador ve a alguien romper las reglas deberá reportarlo al organizador del evento o a un administrador.</li>
</ul></p>

<h2>Comportamiento</h2>
<h3>Obligación Moral del Jugador</h3>
<p>El comportamiento deportivo es responsabilidad de todos los participantes del torneo y promueve la sana competición entre los jugadores. El incumplimiento de esto puede terminar en que los jueces de RCS le ordenen a un jugador abandonar el torneo o, incluso, todos los eventos de RCS.</p>
<p>Todos los participantes del torneo, incluyendo jugadores, administradores y el organizador del torneo deben leer y adherirse a estas reglas, dentro y fuera del juego.</p>

<h3>Jueces del torneo</h3>
<p>Los organizadores y administradores deberán dar el ejemplo y adherirse al comportamiento descrito en este documento. Esto asegura una experiencia justa y consistente para todos los jugadores durante el torneo.</p>
<p>Los organizadores y administradores deberán ser imparciales y transparentes cuando se hacen decisiones oficiales. Ellos deberán comunicarse claramente con los jugadores, especialmente para dar instrucciones específicas o penalizaciones.</p>

<p>Los organizadores y administradores deberán comunicar de buena fe cualquier decisión que se haga respecto al torneo. Todos los torneos oficiales tienen el mismo objetivo: Proveer una experiencia justa y consistente para todos los jugadores participantes del torneo.</p>

<p>Los administradores son la autoridad final para determinar infracciones y penalizaciones. Además, los aministradores tienen la última palabra para llevar acabo los procesos del torneo. Estos incluyen, pero no se limitan a: dar penalizaciones por incumpliemiento de las reglas, revisar la lista de cartas de los jugadores, imponer reglas de formato para los mazos, guiar apropiadamente el final de un juego y asegurarse de que los resultados de cada partida son registrados correctamente.</p>
<p>Los administradores son la autoridad final en decisiones de logística del torneo. Esto incluye, pero no se limita a: elección de locales, organizar a los “casters”, asegurarse que el equipo relacionado al torneo funciona apropiadamente, registros al torneo, emparejamiento de oponentes, la extensión y cronograma del torneo, y los premios. </p>

<h3>Comunicación entre jugadores</h3>
<p>Los jugadores están sujetos a los siguientes lineamientos. </p>

<p>Comunicación asertiva con los jueces del torneo, los capitanes de equipo y demás, para una experiencia de torneo transparente.</p>

<p>Los jugadores pueden comunicarse entre sí, siempre y cuando no violen ninguna regla escrita en este documento.</p>

<h2>Retroalimentación y quejas</h2>
<p>Los jugadores pueden apelar cualquier decisión de cualquier réferi con el réferi principal. Si el réferi principal no se encuentra en el torneo el comisionado de liga o el vicecomisionado de liga será el representante de la autoridad.</p>
<p>Para retroalimentación de los eventos hacer click aquí <a href="info.php">info portal</a>.</p>

<h2> Administradores </h2>

<h3>Penalizaciones</h3>
<p>Los réferis que observen infracciones durante el torneo deberán dar penalizaciones basados en los lineamientos de este documento. Los dueños de locales, organizadores o cualquier otro deberán informar a los jueces en lugar de dar penalizaciones ellos mismos.</p>
<p>Los réferis deberán comunicar las infracciones, penalizaciones y cualquier otra información pertinente al jugador ofendido, a su equipo y al equipo contrario de manera clara y concisa.</p>

<p>Los réferis deberán determinar una infracción antes de aplicar una penalización.</p>

<p>Los réferis deberán tomar decisiones imparciales.</p>

<p>Las penalizaciones podrían aplicar, bien durante solo un encuentro, o durante todo el torneo.</p>

<p>Los equipos no podrán condonar las penalizaciones de sus oponentes.</p>

<p>Las penalizaciones incluyen:</p>

<ul>

<li>Advertenia: cuando un juez ve a un jugador cometiendouna infracción menor. Deben ser anotadas, para futuras penalizaciones por reiteración.</li>

<li>juego perdido: infracciones severas forzarán al jugador a tomar un juego perdido de manera automática.</li>

<li>Descalificación: infracciones graves harán que el jugador sea descalificado del torneo. Un jugador descalificado perderá sus premios a menos que su descalificación sea el resultado de acumular «Advertencias», en ese caso recibirá premios en relación a su posición final.</li>
</ul>

<p>A menos que se indique lo contrario las penalizaciones se jerarquizan de la siguiente forma: Advertencia-Advertencia-Perdida de juego-Perdida de ronda-descalificación.</p>


<h2>Infracciones</h2>
<h3>Tardías</h3>
<p>Esta infracción ocurre cuando un jugador no está listo al inicio de un juego..</p>

<p>Los jugadores necesitan estar a tiempo para jugar cuando la ronda inicia. Cuando un jugador se retrasa, retrasa todo el torneo.</p>

<p>Si un jugador llega 15 minutos tarde será descalificado.</p>

<h3>Intervención de terceros</h3>
<p>Cuando un jugador se comunica con alguien durante su juego y esto le da una irremediable ventaja sobre su oponente.</p>
<p>Esta infracción asume que el jugador no estaba tratando de hacer trampa de manera deliberada.</p>
<p>De lo contrario el jugador será penalizado con una advertencia por comportamiento antideportivo.</p>

<h3>No seguimiento de instrucciones</h3>
<p>Cada jugador tiene la responsabilidad de seguir las instrucciones del torneo. Si los responsables tienen que repetir demasiadas veces las instrucciones el curso del torneo se verá alterado, esto provocará retrasos y disputas indeseadas. La penalización por no seguir instrucciones será un juego perdido.</p>

