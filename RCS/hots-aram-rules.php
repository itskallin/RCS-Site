<h2><em>Tournament Administrative Rules</em></h2>
<h3>Competitor Code of Conduct</h3>
<p>Players are expected to maintain a level of civility toward other competitors and the RCS Heroes of the Storm administration. Violations of this rule will be dealt with on a case by case basis, and repeated failures to abide may result in penalties toward the associated team.</p>
<p>Players are expected to act in accordance with general good will. We will not tolerate any toxic behavior. Those found disrupting the order of the tournament or harassing a player in any way will be removed from the tournament while more egregious conduct may result in additional actions taken against both the player and their team if applicable.</p>
<p>Any attempt to deceive other players or the administration with false information, fake accounts, teams or matches will result in a lifetime ban from all RandomCS activities. As a community, we do not want such people playing.</p>
<p>Cheating or using any known exploits or bugs to your advantage in an RCS Heroes of the Storm match will result in forfeit from the tournament, a season ban and point forfeiture. Any further infractions after the ban period will result in a lifetime ban.</p>
<p><em>Ignorance of the rules is not an excuse.</em></p>
<h3>Roster</h3>
<p>All players must be added to the team on battlefy.com prior to the start of a tournament, this includes up to two stand-in substitutes players. Points are tied to the Team Captain and the Team Name for the season. A Tournament Admin will need to be notified of any Team Name changes for points to transfer.</p>
<h3>Server</h3>
<p>All RCS matches will be played on ORD 1.
</p>
<h3>Failure to appear / No Show</h3>
<p>Each team has 15 minutes to field a full team. Please report no-show teams to Tournament Admins in Discord. Team Captains should contact the Tournament Admins if conflicts arise due to prior engagements.
</p>
<h3>Penalty for dropping out from a tournament

</h3>
<p>If a team drops from a weekly event or violates the 15 minute match start rule, they will not be permitted to re-enter that week's tournament.
</p>

<h3>Protest</h3>
<p>A protest should be made through the Discord server, a private message may be sent to an admin if public chat is not desired.</p>
<p>If a player wants to submit a protest concerning a specific match, the protest needs to be filed before the start of the match winners next game.  A formal complaint regarding behaviour and/or bugs needs to occur no more than 15 minutes after the game ended, otherwise the matter will be considered as closed.  Remember that screenshots or replays may be requested so be prepared to provide the necessary documentation.
</p>
<h3>Pause Rules

</h3>
<p>Game pauses should only occur if a player is having connection issues. If more than ten minutes are needed, petition an admin in IRC and include what the circumstances are and what is being done to try and fix them. Teams will announce unpausing prior to actually doing so.
</p>
<p>Avoid pausing during a teamfight if at all possible.
</p>
<h3>Spirit of the Game / Rules

</h3>
<p>The Tournament Administration reserves the rights to make judgment on extenuating circumstances that come up as they deem fit. Any major changes to rules will be announced, and the rules will be amended accordingly. Any ineligible talents will be announced in Discord during the check in phase.
</p>
<h3>Score Reporting

</h3>
<p>After a match is concluded the game replay must be uploaded to the Battlefy match reporting screen provided on the tournament website. The winning team will submit the score. Teams are to keep a copy of a replay for at least two weeks, in the event of a formal protest.
To report a score:</p>
<p>
<ul>
<li>Find the match on the tournament bracket and left click to select that match.</li>
<li>Click the Choose File and navigate to where the replay files are saved.</li>
<li>Alternatively, the score can be manually enter and hit submit</li>
<li>Click OK and upload the file.</li>
</ul>
</p>
<p>Any problems or trouble uploading, please contact a Tournament Admin.</p>
<h3>Rematch/Remake</h3>

<p>In the event of a tournament running exceptionally late, players can petition Tournament Admins to finish earlier the next evening. This is at the admins discretion to allow.</p>
<p>If a player for some reason cannot continue the game, the game may be remade provided that the request for remake happened before the ONE minute mark after the first minion wave spawned.  A Tournament Admin must be notified of the issue.</p>
 
<p>In case of a remake, in an ARAM tournament, the heroes that were previously auto selected will need to be selected for the remake.</p>
<p>Rematch and Remakes (past the allotted time) cannot be requested for a player having connectivity issues.  If a player has disconnected then they have the amount of issued pause time to return to the game. If the player is unable to reconnect, the game may be played out with the game AI bot.</p>
<h3>Spectators</h3>

<p>General spectators will not be allowed in a game. The only authorized spectators are Tournament Administration or official broadcasters.</p>
<h3>Prize Distribution</h3>

<p>The Team Captain will be responsible for the distribution of the prize and will be paid out through Paypal.</p>
<h3>Tournament Proceedings</h3>

<p>Games will be best of one until the finals which will be a best of three.</p>
<h3>ARAM Rules</h3>
<ul>
<li>Games will be best of one until the finals which will be a best of three.</li>
<li>Lost Caverns must be selected as the map.</li>
<li>Players must choose ‘Auto-Select’ as their hero</li>
<li>If SGT. Hammer is randomed, Orbital BFG (the level 20 BFG upgrade) is off-limits.</li>
<li>Mule is off-limits.</li>
<li>Zagara’s Nydus Worm is off-limits.</li>
<li>Picking any of these talents will result in an instant disqualification, even if by accident.</li>
<li>Health Orbs that spawn around the map are fair game to whoever collects them first.</li>
<li>A minimum of TEN heroes is required on the Heroes of the Storm game account.</li>
</ul>
<h3>Tournament Direction</h3>

<p>The RCS Heroes of the Storm is organized by the RandomCS.com Heroes of the Storm division (RCS).</p>
<h3>Rule modifications</h3>

<p>The RCS Heroes of the Storm administration reserves the right to change or modify these rules, and may at their discretion choose to make decisions not explicitly outlined by these rules in order to keep things fair and with good sportsmanship.</p>
<p>The above rules are to be seen as a guideline but can be updated by head admin at any time, to reflect last minute changes or clarifications/modifications as required.</p>

<h3>Discord</h3>

<p>All players are welcome to be present on the Discord server. Teams captains will need to announce themselves and their team within Discord. The Tournament Admin will be labeled as such within the Discord Chat.</p>
<p>Contact admins on discord:</p><ul>
<li>Dvexx | GG #0606</li>
<li>ManMode | GG #6027</li></ul>
<h3>Game broadcasts</h3>


<p>RandomCS reserves all rights to broadcasting its own events. 3rd parties are not allowed to stream RCS events, without prior authorization from the organization.</p>
<p>Individual players ARE allowed to broadcast *their own* matches if and only if the following conditions are met:</p><ul>
<li>The match is not already being broadcast by the organization.</li>
<li>They enable a minimum two minute delay on their stream.</li>
</ul>
